    <h3>History of the Council</h3>
    <p>
        Colombo is a charter city, with a Mayor and Council form of government. Colombo's mayor and the council
        members are elected through local government elections held once in four years. For the past 50 years the city
        had been ruled by the United National Party (UNP), whose business friendly policies resonate with the
        population of Colombo.
    </p>

    <p>
        Colombo Municipal Council (CMC) is the largest Local Authority in Sri Lanka and one of the oldest in South Asia.
        Established in 1865, it has grown into a large organization catering to the needs of a resident population of
        647,100 (2001 census) and a floating population of nearly 500,000 (estimated).
    </p>

    <p>
        At the time of the establishment of the Municipal Council, the population was in the region of 80,000.
        The revenue of the Colombo Municipal Council in its first year was Sterling Pounds 6,429 and in the year 1872,
        when Ceylon changed over from sterling to a decimal currency, the Council's income had risen to Rs. 296,494/=.
        The main sources of revenue of the Council's income in 1866 were the assessment rates, taxation on vehicles and
        animals, the commutation rate, tolls in streets, bridges and canals, licences of butchers, carriers, wine and
        retail dealers, gun licences, stamp duty on the certificates of proctors, advocates and notaries, market and
        slaughter house fees, fines recovered by the Police and Municipal Magistrates and miscellaneous receipts.
        As the Legislative body of the City, the City Council serves as the link between the citizens of Colombo and
        their municipal government. Through the filing of legislation, the enactment of orders, ordinances, and
        resolutions, the Council actively represents the diverse interests of its residents while ensuring the
        efficient and cost effective delivery of services.
    </p>

    <p>
        Today the council with 15 Departments is mainly responsible for the provision of services such as public health
        and curative services, solid waste management, maintenance of roads, Lands and Environmental Development,
        street lighting, water and drainage, and veterinary services. In addition to these there are number of
        Departments providing social services, sports and recreation, library services together with finance, rates,
        secretarial and training departments.
    </p>

